def add(a, b):
    return a+b
def sub(a, b):
    return a-b
def mul(a, b):
    return a-b
def div(a, b):
    return a/b

num1 = 10
num2 = 5

addResult = add(10, 5)
print('Add: {} + {} = {}'.format(num1, num2, addResult));

subtractResult = sub(10, 5)
print('Subtract: {} + {} = {}'.format(num1, num2, subtractResult));

mutltiplyResult = mul(10,5)
print('Multiply: {} + {} = {}'.format(num1, num2, mutltiplyResult));

divResult = div(10,5)
print('Multiply: {} + {} = {}'.format(num1, num2, divResult));